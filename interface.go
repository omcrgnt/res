package res

// Builder определяет контракт для создания ресурса.
// Любой тип, реализующий этот интерфейс, может быть использован
// в процессе сборки (Build) для инициализации приложения.
type Builder interface {
	// Build создает экземпляр ресурса. Возвращаемый объект будет
	// автоматически зарегистрирован в реестре под своим типом.
	Build() (any, error)
}
